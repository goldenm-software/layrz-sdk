package enums

// Weekday is the representation of the days of the week.
// This week starts on Sunday.
type Weekday string

const (
	// WeekdaySunday represents Sunday. Index 0
	WeekdaySunday Weekday = "SUNDAY"
	// WeekdayMonday represents Monday. Index 1
	WeekdayMonday Weekday = "MONDAY"
	// WeekdayTuesday represents Tuesday. Index 2
	WeekdayTuesday Weekday = "TUESDAY"
	// WeekdayWednesday represents Wednesday. Index 3
	WeekdayWednesday Weekday = "WEDNESDAY"
	// WeekdayThursday represents Thursday. Index 4
	WeekdayThursday Weekday = "THURSDAY"
	// WeekdayFriday represents Friday. Index 5
	WeekdayFriday Weekday = "FRIDAY"
	// WeekdaySaturday represents Saturday. Index 6
	WeekdaySaturday Weekday = "SATURDAY"
)
